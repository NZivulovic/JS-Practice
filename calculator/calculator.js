function calc(num1, operator, num2) {
    let resultCalculation = 0;
    function operate() {
        const calculation = 0;
        if (operator === '+') {
            calculation = num1 + num2;
        }
        if (operator === '-') {
            calculation = num1 - num2;
        }
        if (operator === '*') {
            calculation = num1 * num2;
        }
        if (operator === '/') {
            calculation = num1 / num2;
        }
        return resultCalculation = calculation;
    }
    operate();
}
